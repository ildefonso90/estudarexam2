#include <unistd.h>

void	ft_is_negative(int n);

void	ft_is_negative(int n)
{
	char	n1;
	char	n2;

	n1 = 'N';
	n2 = 'P';
	if (n >= 0)
	{
		write(1, &n2, 1);
	}
	else
	{
		write(1, &n1, 1);
	}
}
